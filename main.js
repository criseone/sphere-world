function applyPreset(data) {
  if (!data || data.version !== 1) {
    alert("Unsupported preset version.");
    return;
  }
  // Depth + overview flags
  state.depth = !!data.depth;
  state.overview = !!data.overview;
  document.getElementById("depth-toggle").checked = state.depth;
  document.getElementById("overview-toggle").checked = state.overview;
  if (state.depth) useDepthLayers(); else useSingle();
  applySpheresSide();
  // Main camera
  if (data.main) {
    yaw = data.main.yaw ?? 0;
    pitch = data.main.pitch ?? 0;
    mainCam.fov = data.main.fov ?? 75;
    mainCam.updateProjectionMatrix();
    if (Array.isArray(data.main.pos)) mainCam.position.fromArray(data.main.pos);
  }
  // Monitors
  clearMonitors();
  for (const c of (data.cameras ?? [])) addMonitor(c);
}

