import * as THREE from "three";
import { OrbitControls } from "three/addons/controls/OrbitControls.js";
import { TransformControls } from "three/addons/controls/TransformControls.js";

const SIZE_LANDSCAPE = { w: 300, h: 169 }; // 16:9
const SIZE_PORTRAIT  = { w: 169, h: 300 }; // 9:16
const sizeFor = o => (o === "portrait" ? SIZE_PORTRAIT : SIZE_LANDSCAPE);

// ---------- Layers ----------
// Layer 0 = world (everyone sees). Layer 1 = main-only gizmos (frustums).
const LAYER_WORLD = 0;
const LAYER_GIZMO = 1;

// ---------- Shared scene ----------
const scene = new THREE.Scene();
const sphereGeoOuter = new THREE.SphereGeometry(500, 64, 32);
const sphereGeoMid   = new THREE.SphereGeometry(340, 64, 32);
const sphereGeoInner = new THREE.SphereGeometry(200, 64, 32);
const loader = new THREE.TextureLoader();

function loadEquirectTexture(url) {
  const tex = loader.load(url);
  tex.colorSpace = THREE.SRGBColorSpace;
  tex.minFilter = THREE.LinearFilter;
  tex.magFilter = THREE.LinearFilter;
  tex.generateMipmaps = false;
  return tex;
}

// World state lives in a single object so Save/Load can read it cleanly.
const state = {
  depth: false,
  overview: false,
  spheres: [], // active sphere meshes, regenerated by useSingle/useDepthLayers
};

function disposeSpheres() {
  for (const m of state.spheres) {
    scene.remove(m);
    m.material.map?.dispose();
    m.material.dispose();
  }
  state.spheres = [];
}

function spheresSide() {
  return state.overview ? THREE.DoubleSide : THREE.BackSide;
}

function applySpheresSide() {
  for (const m of state.spheres) m.material.side = spheresSide();
}

function useSingle() {
  disposeSpheres();
  const tex = loadEquirectTexture("test.jpg");
  const mat = new THREE.MeshBasicMaterial({ map: tex, side: spheresSide() });
  const mesh = new THREE.Mesh(sphereGeoOuter, mat);
  scene.add(mesh);
  state.spheres.push(mesh);
}

function useDepthLayers() {
  disposeSpheres();
  const layers = [
    { src: "depth_far.png",  geo: sphereGeoOuter, order: 0 },
    { src: "depth_mid.png",  geo: sphereGeoMid,   order: 1 },
    { src: "depth_near.png", geo: sphereGeoInner, order: 2 },
  ];
  for (const l of layers) {
    const tex = loadEquirectTexture(l.src);
    const mat = new THREE.MeshBasicMaterial({
      map: tex,
      side: spheresSide(),
      transparent: true,
      depthWrite: false,
    });
    const mesh = new THREE.Mesh(l.geo, mat);
    mesh.renderOrder = l.order;
    scene.add(mesh);
    state.spheres.push(mesh);
  }
}

useSingle();

