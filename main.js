// ---------- Axes orientation overlay ----------
// Small Tinkercad-style world-orientation gizmo in the corner of the main view.
const axesScene = new THREE.Scene();
const axesHelper = new THREE.AxesHelper(50);
axesHelper.material.depthTest = false;
axesHelper.material.linewidth = 2;
axesScene.add(axesHelper);
const axesCam = new THREE.PerspectiveCamera(45, 1, 0.1, 500);
const _axesDir = new THREE.Vector3();

function renderAxesOverlay() {
  // Position the axes camera so it views the helper from the same direction
  // the main camera looks at the world — the gizmo mirrors the main view.
  mainCam.getWorldDirection(_axesDir);
  axesCam.position.copy(_axesDir).multiplyScalar(-120);
  axesCam.up.copy(mainCam.up);
  axesCam.lookAt(0, 0, 0);

  const w = mainRenderer.domElement.clientWidth;
  const h = mainRenderer.domElement.clientHeight;
  const size = 96;
  const x = 12, y = 12; // bottom-left in WebGL coords
  mainRenderer.setScissorTest(true);
  mainRenderer.setViewport(x, y, size, size);
  mainRenderer.setScissor(x, y, size, size);
  mainRenderer.clear(false, true, false); // clear depth only, in the scissor box
  mainRenderer.render(axesScene, axesCam);
  mainRenderer.setScissorTest(false);
  mainRenderer.setViewport(0, 0, w, h);
}

// ---------- Animation loop ----------
let prev = performance.now();
function loop(now) {
  const dt = Math.min(0.05, (now - prev) / 1000);
  prev = now;
  updateMainCam(dt);
  for (const m of monitors) m.update();
  mainRenderer.clear();
  mainRenderer.render(scene, mainCam);
  renderAxesOverlay();
  for (const m of monitors) m.render();
  requestAnimationFrame(loop);
}
requestAnimationFrame(loop);
